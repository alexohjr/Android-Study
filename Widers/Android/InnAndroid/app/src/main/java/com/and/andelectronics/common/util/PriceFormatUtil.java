package com.and.andelectronics.common.util;

import java.text.NumberFormat;

/**
 * Created by Won on 2017-06-17.
 */
public class PriceFormatUtil
{
    public static String getPriceFormat(String price)
    {
        String formPrice="";
        NumberFormat nf = NumberFormat.getInstance();
        //nf.setMaximumFractionDigits(value);
        formPrice = nf.format(Integer.valueOf(price));
        return formPrice;

    }
}
