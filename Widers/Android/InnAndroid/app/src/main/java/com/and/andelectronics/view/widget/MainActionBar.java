package com.and.andelectronics.view.widget;

import android.content.Context;
import android.util.AttributeSet;
import android.view.LayoutInflater;
import android.widget.LinearLayout;
import android.widget.RelativeLayout;

import com.and.andelectronics.R;

/**
 * Created by Won on 2016-01-02.
 */
public class MainActionBar extends LinearLayout {


    private LayoutInflater mInflater;
    private RelativeLayout actionView;


    public MainActionBar(Context context, AttributeSet attrs) {
        super(context, attrs);

        mInflater = (LayoutInflater) context.getSystemService(Context.LAYOUT_INFLATER_SERVICE);
        actionView = (RelativeLayout) mInflater.inflate(R.layout.widget_main_actionbar, null);
        addView(actionView);
    }
}
